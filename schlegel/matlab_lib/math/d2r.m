function r = d2r(d)
% d2r
% 
% Description:	convert an angle from degrees to radians
% 
% Syntax:	r = d2r(d)
% 
% In:
% 	d	- the angle, in degrees
% 
% Out:
% 	r	- the angle, in radians
% 
% Updated:	2008-11-10
% Copyright 2008 Alex Schlegel (schlegel@gmail.com).  All Rights Reserved.
r	= d*pi/180;
